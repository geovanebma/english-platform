function normalizeWord(value = "") {
  return value
    .normalize("NFKD")
    .replace(/[\u0300-\u036f]/g, "")
    .trim()
    .toLowerCase();
}

function parseWikiWords(raw) {
  return raw
    .split(/\r?\n/)
    .map((line) => line.trim())
    .filter((line) => line && !line.startsWith("#!comment") && !/^\d+\s*-\s*\d+$/.test(line))
    .map((word, index) => ({
      rank_order: index + 1,
      word,
      normalized_word: normalizeWord(word),
    }));
}

async function seedWikiWords() {
  const existing = await pool.query(`SELECT COUNT(*)::int AS total FROM english_wiki_words`);
  if (Number(existing.rows[0]?.total || 0) > 0) {
    return;
  }

  const raw = await fs.readFile(WIKI_WORDS_PATH, "utf8");
  const words = parseWikiWords(raw);
  const chunkSize = 2000;

  for (let index = 0; index < words.length; index += chunkSize) {
    const chunk = words.slice(index, index + chunkSize);
    const values = [];
    const placeholders = chunk
      .map((item, rowIndex) => {
        const offset = rowIndex * 3;
        values.push(item.rank_order, item.word, item.normalized_word);
        return `($${offset + 1}, $${offset + 2}, $${offset + 3})`;
      })
      .join(", ");

    await pool.query(
      `INSERT INTO english_wiki_words (rank_order, word, normalized_word)
       VALUES ${placeholders}
       ON CONFLICT (word) DO NOTHING`,
      values
    );
  }
}

